'''
1.判断空气质量:
	pm2.5 > 75 重度污染,提示市民不宜出行
	35< pm2.5 <= 75 空气良好,适当户外活动
	pm2.5<=35 空气质量优，赶紧出来嗨

2.当用户输入pm2.5值，只提示当前空气是否污染
'''

pm = eval(input("请输入pm2.5的值:"))

if pm > 75:
	print('重度污染,提示市民不宜出行')
elif 35 < pm <= 75:
	print('空气良好,适当户外活动')
else:
	print('空气质量优，赶紧出来嗨')

print("空气{}污染".format("是" if pm > 75 else "没有"))

